import turtle
wn = turtle.Screen()
germ = turtle.Turtle()
germ.color("black")
germ.turtlesize(0.0001)
germ.speed(0)
germ.color("red")
germ.penup()
germ.setposition(-300,-200)
germ.begin_fill()
for i in range (1,3):
    germ.forward(600)
    germ.left(90)
    germ.forward(400)
    germ.left(90)
germ.end_fill()
germ.penup()
germ.setposition(-230,120)
germ.pendown()
germ.color("yellow")
germ.setheading(0)
germ.right(45)
germ.circle(40,135)
wn.mainloop()